public class Alumno {
    private int MAT;
    private double CAL1, CAL2, CAL3, CAL4, CAL5;

    public Alumno() {
    }

    public Alumno(int MAT, double CAL1, double CAL2, double CAL3, double CAL4, double CAL5) {
        this.MAT = MAT;
        this.CAL1 = CAL1;
        this.CAL2 = CAL2;
        this.CAL3 = CAL3;
        this.CAL4 = CAL4;
        this.CAL5 = CAL5;
    }

    public int getMAT() {
        return MAT;
    }

    public void setMAT(int MAT) {
        this.MAT = MAT;
    }

    public double getCAL1() {
        return CAL1;
    }

    public void setCAL1(double CAL1) {
        this.CAL1 = CAL1;
    }

    public double getCAL2() {
        return CAL2;
    }

    public void setCAL2(double CAL2) {
        this.CAL2 = CAL2;
    }

    public double getCAL3() {
        return CAL3;
    }

    public void setCAL3(double CAL3) {
        this.CAL3 = CAL3;
    }

    public double getCAL4() {
        return CAL4;
    }

    public void setCAL4(double CAL4) {
        this.CAL4 = CAL4;
    }

    public double getCAL5() {
        return CAL5;
    }

    public void setCAL5(double CAL5) {
        this.CAL5 = CAL5;
    }
}
